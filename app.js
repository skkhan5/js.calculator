let a = +prompt("Enter First Number")
let opratar = prompt("select oprator (+,-, /,*):")
let b = +prompt("Enter Second Number")

if( opratar == "+"){
    document.write(a + b)
}
if( opratar == "-"){
    document.write(a - b)
}
if( opratar == "*"){
    document.write(a * b)
}
if( opratar == "/"){
    document.write(a / b)
}